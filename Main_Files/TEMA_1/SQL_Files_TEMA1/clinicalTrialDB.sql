DROP TABLE TREATMENTS;
DROP TABLE CAREGIVERS;
DROP TABLE CLINICIANS;
DROP TABLE PACIENTS;

CREATE TABLE PACIENTS(
  idpacient INTEGER  NOT NULL ,
  subject INTEGER NOT NULL,
  website INTEGER NOT NULL,
  gender varchar2(255) default NULL,
  age varchar2(50) default NULL,
  doctor varchar2(255) default NULL,
  clinic varchar2(255) default NULL,
  symptoms varchar2(255) default NULL,
  CONSTRAINT pacient_pk PRIMARY KEY ( idpacient )
);

CREATE TABLE CAREGIVERS (
  CAREGIVER  INTEGER  NOT NULL ,
  NAME varchar2(255) default NULL,
  AGE varchar2(50) default NULL,
  JOB varchar2(255) default NULL,
  TELEPHONE varchar2(100) default NULL,
  SUBJECT number default NULL,
  CONSTRAINT caregiver_pk PRIMARY KEY ( caregiver )
);

CREATE TABLE CLINICIANS (
  id INTEGER  NOT NULL,
  CLINICIAN number default NULL,
  NAME varchar2(255) default NULL,
  SPECIALITY varchar2(255) default NULL,
  TELEPHONE varchar2(100) default NULL,
  PRIMARY KEY (id)
);

CREATE TABLE TREATMENTS (
  id INTEGER  NOT NULL,
  TREATMENT number,
  CLINICIAN number default NULL,
  SUBJECT number default NULL,
  DRUG BLOB default NULL,
  PRIMARY KEY (id)
); 

